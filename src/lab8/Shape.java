package lab8;

import java.awt.*;
public abstract class Shape {
    private Color color;
    private int x;
    private int y;
    private int width;
    private int height;

    public Color getColor() {
        return this.color;
    }
    public int getX() {
        return this.x;
    }
    public int getY() {
        return this.y;
    }
    public int getWidth() {
        return this.width;
    }
    public int getHeight() {
        return this.height;
    }

    public void setColor(Color color) {
        this.color = color;
    }
    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }
    public void setWidth(int width) {
        this.width = width;
    }
    public void setHeight(int height) {
        this.height = height;
    }
    public void draw(Graphics g){}
}
